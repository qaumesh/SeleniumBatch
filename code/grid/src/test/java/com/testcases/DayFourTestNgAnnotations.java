package com.testcases;

import org.testng.annotations.AfterClass;
import org.testng.annotations.AfterMethod;
import org.testng.annotations.BeforeClass;
import org.testng.annotations.BeforeMethod;
import org.testng.annotations.Test;

public class DayFourTestNgAnnotations {

	
	@AfterClass
	public void afterclass() {
		System.out.println("after class - @AfterClass");
	}
	
	@BeforeClass
	public void beforeclass() {
		System.out.println("before class - @BeforeClass");
	}
	
	@BeforeMethod
	public void beforeMethod() {
		System.out.println("before method - @BeforeMethod");
	}
	
	@AfterMethod
	public void afterMethod() {
		System.out.println("after method - @AfterMethod");
	}
	
	@Test
	public void test() {
		System.out.println("test - @Test");
	}
	
	
	@Test
	public void car() {
		System.out.println("car - @Test");
	}
	
	@Test
	public void demo() {
		System.out.println("demo - @Test");
	}
	
	@Test
	public void bike() {
		System.out.println("bike - @Test");
	}
	
}
